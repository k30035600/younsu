    ma_end = head.index("\n\n### 바.", ma_start)
    head = head[:ma_start] + NEW_MA + head[ma_end:]

    head = head.replace("[검토]", "[작업]")
    head = head.replace(
        "(`행정심판청구/(국가법령정보)판례모음/11_98두4061.pdf` 및 law.go.kr 원문 대조)",
        "(국가법령정보센터 원문과 대조)",
    )
